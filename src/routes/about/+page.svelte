    <h3>My Story</h3>
    <p>
        <span class="pl-6">RomeWithMe</span> began as a personal  blog during my semester abroad in Rome, but the idea behind it
        was born much earlier — in the hesitation, the fears, and the “what ifs” that nearly stopped
        me from going in the first place. The thought of leaving home to step into a whole new
        continent was intimidating. I didn’t know the language, I didn’t know the people, and I had
        no idea what it would feel like to suddenly be surrounded by centuries of unfamiliar culture.
        Money was also a worry — without steady income abroad, I had to save, find external funding,
        and plan every expense carefully. And then there was the cultural gap: what if I offended
        someone without meaning to, just because I didn’t understand their traditions?
    </p>

    <p>
        <span class="pl-6">When</span> I finally stepped off the plane, the weight of my fears didn’t vanish all at once, they 
        quickly began to unravel. However the strangers I met in those first days weren’t distant or unwelcoming;
        they were patient, open, and curious. Every conversation reminded me that kindness and connection 
        transcend language barriers. And then there was Rome itself: standing in a city layered with 
        thousands of years of history. I was living at the center of a big part of human civilization. 
        That perspective reshaped how I saw my own small place in the world.
    </p>

    <p>
        <span class="pl-6">Life</span> abroad didn’t come with training wheels. Independence wasn’t just about cooking or shopping,
        it was about doing all of that while navigating a new culture, a new language, and a completely
        unfamiliar system. From planning trips across countries to balancing classes with daily survival 
        tasks, every choice forced me to adapt quickly and rely on myself in ways I never had to back home.
        That growth made me resourceful, confident, and resilient. Along the way, I built friendships that 
        crossed borders and time zones, proof that no matter how foreign a place feels at first, you can 
        always find connection.
    </p>

    <p>
        <span class="pl-6">RomeWithMe</span> was born from that transformation. I didn’t create this platform for profit or
        recognition — I created it because I believe every student should have the chance to feel what
        I felt. The world is vast, and if you have even one opportunity to step outside your comfort
        zone and see it, you should take it. My goal is simple: to encourage and support students in
        taking that leap.
    </p>

    <h2>Our Mission</h2>
    <p>
      Our mission is to build a supportive, resource-rich, and welcoming community for students who
      are curious about studying abroad. Whether you are still on the fence, planning your first
      semester overseas, or already living abroad, RomeWithMe exists to connect you with others who
      understand the journey. We aim to make studying abroad less intimidating by providing access
      to scholarships, resources, and — most importantly — the real stories of students who’ve gone
      before you.
    </p>

    <h2>Our Values</h2>
    <ul>
